package fr.codeonce.grizzly.core.domain.user;

public class PaymentMethod {

    private String lastFourNumber;
    private String expMonth;
    private String expYear;
    private String cardHolder;
    private String cardNature;
    private String paymentMethodId;
    private String customerId;


    public String getCustomerId() {
        return customerId;
    }

    public void setCustomerId(String customerId) {
        this.customerId = customerId;
    }

    public String getLastFourNumber() {
        return lastFourNumber;
    }

    public void setLastFourNumber(String lastFourNumber) {
        this.lastFourNumber = lastFourNumber;
    }

    public String getExpMonth() {
        return expMonth;
    }

    public void setExpMonth(String expMonth) {
        this.expMonth = expMonth;
    }

    public String getExpYear() {
        return expYear;
    }

    public void setExpYear(String expYear) {
        this.expYear = expYear;
    }

    public String getCardHolder() {
        return cardHolder;
    }

    public void setCardHolder(String cardHolder) {
        this.cardHolder = cardHolder;
    }

    public String getCardNature() {
        return cardNature;
    }

    public void setCardNature(String cardNature) {
        this.cardNature = cardNature;
    }

    public String getPaymentMethodId() {
        return paymentMethodId;
    }

    public void setPaymentMethodId(String paymentMethodId) {
        this.paymentMethodId = paymentMethodId;
    }


}
